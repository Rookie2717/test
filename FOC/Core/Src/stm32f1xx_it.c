void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {

    if (huart->Instance == huart1.Instance)  //
    {
        Res = USART1->DR;
        if ((USART_RX_STA & 0x8000) == 0)//是否接收完成
        {
            if (USART_RX_STA & 0x4000)//是否接收到0x0d
            {
                if (Res != 0x0a)USART_RX_STA = 0;//未接收到0x0a则无效，数据清零
                else USART_RX_STA |= 0x8000;    //接收到0x0d数据有效，并将USART_RX_STA最高位置1表示接收已经完成
            }
            else //未接收到则判断是否接收到了0x0d如果接收到了则将bit14置1，否则存入BUF中
            {
                if (Res == 0x0d)USART_RX_STA |= 0x4000;
                else
                {
                    USART_RX_BUF[USART_RX_STA & 0X3FFF] = Res;
                    USART_RX_STA++;
                    if (USART_RX_STA > (USART_REC_LEN - 1))USART_RX_STA = 0;//如果一帧数据超出缓冲区范围则清零
                }
            }
        }
        HAL_UART_Receive_IT(&huart1, &Res, 1);
    }
}
